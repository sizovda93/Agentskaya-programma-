// ─── PUBLIC API ─────────────────────────────────────────

const contentByRole: Record<UserRole, LearningModule[]> = {
  agent: agentModules,
  manager: managerModules,
  admin: adminModules,
};

export function getModules(role: UserRole): LearningModule[] {
  return contentByRole[role] ?? [];
}

export function getLesson(role: UserRole, slug: string): { module: LearningModule; lesson: LearningLesson } | null {
  const modules = contentByRole[role] ?? [];
  for (const mod of modules) {
    const lesson = mod.lessons.find((l) => l.slug === slug);
    if (lesson) return { module: mod, lesson };
  }
  return null;
}

export function getAllLessons(role: UserRole): { module: LearningModule; lesson: LearningLesson }[] {
  const modules = contentByRole[role] ?? [];
  const result: { module: LearningModule; lesson: LearningLesson }[] = [];
  for (const mod of modules) {
    for (const lesson of mod.lessons) {
      result.push({ module: mod, lesson });
    }
  }
  return result;
}
